    {
    if ( self = [ super initWithWindowNibName: @"TWPMainWindow" ] )
        {
        // TODO:
        }

    return self;
    }

#pragma mark Conforms <NSNibAwaking> protocol
- ( void ) awakeFromNib
    {
    // TODO:
    }

#pragma mark IBActions
- ( IBAction ) collapsedTweetingBoxAction: ( id )_Sender
    {
    [ [ NSNotificationCenter defaultCenter ] postNotificationName: TWPTweetingBoxShouldBeCollapsed
                                                           object: self
                                                         userInfo: nil ];
    }

- ( IBAction ) tweetAction: ( id )_Sender
    {
    [ [ [ TWPLoginUsersManager sharedManager ] currentLoginUser ].twitterAPI
        postStatusUpdate: self.tweetTextField.stringValue
       inReplyToStatusID: nil latitude: nil longitude: nil placeID: nil displayCoordinates: nil trimUser: @NO
            successBlock:
        ^( NSDictionary* _Status )
            {

            } errorBlock:
                ^( NSError* _Error)
                    {
                    NSLog( @"%@", _Error );
                    } ];
    }

