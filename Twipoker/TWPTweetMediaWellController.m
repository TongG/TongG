#import "TWPTweetMediaWellController.h"
#import "TWPTweetMediaWell.h"

// Private Interfaces
@interface TWPTweetMediaWellController ()

- ( TWPTweetMediaWell* ) _mediaWell;

@end // Private Interfaces

// TWPTweetMediaWellController class
@implementation TWPTweetMediaWellController

@dynamic mediaWell;
@dynamic tweet;

#pragma mark Initializations
+ ( instancetype ) mediaWellControllerWithTweet: ( OTCTweet* )_Tweet
    {
    return [ [ [ self class ] alloc ] initWithTweet: _Tweet ];
    }

- ( instancetype ) initWithTweet: ( OTCTweet* )_Tweet
    {
    if ( self = [ super initWithNibName: @"TWPTweetMediaWell" bundle: [ NSBundle mainBundle ] ] )
        {
        self->_tweet = _Tweet;
        [ [ self _mediaWell ] setTweet: _Tweet ];
        }

    return self;
    }

#pragma mark Dynamic Accessors
- ( TWPTweetMediaWell* ) mediaWell
    {
    return [ self _mediaWell ];
    }

- ( void ) setTweet: ( OTCTweet* )_Tweet
    {
    self->_tweet = _Tweet;
    [ [ self _mediaWell ] setTweet: _Tweet ];
    }

- ( OTCTweet* ) tweet
    {
    return self->_tweet;
    }

#pragma mark Private Interfaces
- ( TWPTweetMediaWell* ) _mediaWell
    {
    return ( TWPTweetMediaWell* )[ self view ];
    }

@end // TWPTweetMediaWellController class

